import numpy


class Calculator:

    def __init__(self):
        print("Welcome to the most basic calculator in the world")

    # Adds two numbers
    def add(self, x, y):
        return x + y
    
    # Subtract two numbers
    def subtract(self, x, y):
        return x - y
    
    # Multiply two numbers 
    def multiply(self, x, y): 
        return x * y
